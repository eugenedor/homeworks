--(+)--
UPDATE RS_PRICE
SET DateEnd = DATEADD(dd, 
	          DATEDIFF(dd, DateEnd, CONVERT(DATETIME, CONVERT(NVARCHAR(4), YEAR(DateEnd)+1) + '0101', 112))-1, 
			  CONVERT(DATETIME, CONVERT(NVARCHAR(12), DateEnd, 112), 112))
WHERE PriceId = 1;
